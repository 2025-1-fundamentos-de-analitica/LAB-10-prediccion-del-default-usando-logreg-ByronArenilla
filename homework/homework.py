import pandas as pd
import numpy as np
import os
import json
import gzip
import pickle

from sklearn.model_selection import GridSearchCV
from sklearn.pipeline import Pipeline
from sklearn.compose import make_column_transformer
from sklearn.preprocessing import OneHotEncoder, MinMaxScaler
from sklearn.feature_selection import SelectKBest, f_classif
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import precision_score, recall_score, f1_score, balanced_accuracy_score, confusion_matrix

# ------------------------------ FUNCIONES DE UTILIDAD ------------------------------ #

def cargar_datos(path: str) -> pd.DataFrame:
    return pd.read_csv(path, compression="zip")

def limpiar_datos(df: pd.DataFrame) -> pd.DataFrame:
    df = df.rename(columns={"default payment next month": "default"})
    df = df.drop(columns=["ID"], errors="ignore")
    df = df[(df["MARRIAGE"] != 0) & (df["EDUCATION"] != 0)]
    df["EDUCATION"] = df["EDUCATION"].apply(lambda val: val if val <= 4 else 4)
    df = df.dropna()
    return df

def preparar_datos(df: pd.DataFrame):
    X = df.drop(columns=["default"])
    y = df["default"]
    return X, y

def crear_pipeline(X: pd.DataFrame) -> Pipeline:
    cat_cols = X.select_dtypes(include=["object", "category"]).columns.tolist()

    preprocesamiento = make_column_transformer(
        (OneHotEncoder(handle_unknown="ignore", sparse_output=False), cat_cols),
        remainder="passthrough"
    )

    return Pipeline([
        ("preprocessing", preprocesamiento),
        ("scaling", MinMaxScaler()),
        ("feature_selection", SelectKBest(score_func=f_classif, k=10)),
        ("classifier", LogisticRegression(solver="liblinear", max_iter=500, random_state=42))
    ])

def ajustar_modelo(pipeline: Pipeline, X, y) -> GridSearchCV:
    params = {
        "feature_selection__k": range(1, 11),
        "classifier__C": [0.001, 0.01, 0.1, 1, 10],
        "classifier__penalty": ["l1", "l2"]
    }

    grid = GridSearchCV(
        estimator=pipeline,
        param_grid=params,
        scoring="balanced_accuracy",
        cv=10,
        n_jobs=-1,
        refit=True,
        verbose=1
    )
    grid.fit(X, y)
    return grid

def guardar_modelo(estimator: GridSearchCV, path: str):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with gzip.open(path, "wb") as f:
        pickle.dump(estimator, f)

def calcular_metricas(y_verdad, y_pred, nombre_dataset: str) -> dict:
    return {
        "type": "metrics",
        "dataset": nombre_dataset,
        "precision": precision_score(y_verdad, y_pred),
        "balanced_accuracy": balanced_accuracy_score(y_verdad, y_pred),
        "recall": recall_score(y_verdad, y_pred),
        "f1_score": f1_score(y_verdad, y_pred)
    }

def calcular_matriz_confusion(y_verdad, y_pred, nombre_dataset: str) -> dict:
    cm = confusion_matrix(y_verdad, y_pred)
    return {
        "type": "cm_matrix",
        "dataset": nombre_dataset,
        "true_0": {"predicted_0": int(cm[0, 0]), "predicted_1": int(cm[0, 1])},
        "true_1": {"predicted_0": int(cm[1, 0]), "predicted_1": int(cm[1, 1])}
    }

# ------------------------------ FLUJO PRINCIPAL ------------------------------ #

def main():
    path_entrada = "files/input/"
    path_modelo = "files/models/model.pkl.gz"
    path_salida = "files/output/metrics.json"

    df_train = limpiar_datos(cargar_datos(os.path.join(path_entrada, "train_data.csv.zip")))
    df_test = limpiar_datos(cargar_datos(os.path.join(path_entrada, "test_data.csv.zip")))

    X_train, y_train = preparar_datos(df_train)
    X_test, y_test = preparar_datos(df_test)

    modelo = crear_pipeline(X_train)
    modelo_entrenado = ajustar_modelo(modelo, X_train, y_train)

    guardar_modelo(modelo_entrenado, path_modelo)

    y_pred_train = modelo_entrenado.predict(X_train)
    y_pred_test = modelo_entrenado.predict(X_test)

    resultados = [
        calcular_metricas(y_train, y_pred_train, "train"),
        calcular_metricas(y_test, y_pred_test, "test"),
        calcular_matriz_confusion(y_train, y_pred_train, "train"),
        calcular_matriz_confusion(y_test, y_pred_test, "test")
    ]

    os.makedirs(os.path.dirname(path_salida), exist_ok=True)
    with open(path_salida, "w", encoding="utf-8") as f:
        for resultado in resultados:
            f.write(json.dumps(resultado) + "\n")

if __name__ == "__main__":
    main()
